void Endings::Start()
{
    char input;

	//std::cout << ending1 << std::endl;
    bool isExit = false;
    Scene::GotoXY(25, 10);
    cout << "Please Input '1,2,3,4,5' To Check The Unlocked Endings, Enter 0 to exit";
    while (!isExit) {

        input = _getch();
        switch (input)
        {
        case '1':
            system("cls");
            ending1();
            cout << "Please Input '1,2,3,4,5' To Check The Unlocked Endings, Enter 0 to exit";
            break;
        case '2':
            system("cls");
            ending2();
            cout << "Please Input '1,2,3,4,5' To Check The Unlocked Endings, Enter 0 to exit";
            break;
        case '3':
            system("cls");
            ending3();
            cout << "Please Input '1,2,3,4,5' To Check The Unlocked Endings, Enter 0 to exit";
            break;
        case '4':
            system("cls");
            ending4();
            cout << "Please Input '1,2,3,4,5' To Check The Unlocked Endings, Enter 0 to exit";
            break;
        case '5':
            system("cls");
            ending5();
            cout << "Please Input '1,2,3,4,5' To Check The Unlocked Endings, Enter 0 to exit";
            break;
        case '0':
            SceneManager::LoadScene(new MainMenu());
            isExit = true;
            break;
        }

    }
}


